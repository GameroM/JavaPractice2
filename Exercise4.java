// Write a Java Program to print the result of the following operations:
// -5+8*6
// (55+9) %9
// 20+-3*5/8
// 5+15/3*2-8%3

package exercises1;

public class Exercise4 {
	public static void main(String[] args) {
		System.out.println("The result of -5+8*6 is: "+(-5+8*6) );
		System.out.println("The result of (55+9)%9 is: "+((55+9)%9));
		System.out.println("The result of 20+-3*5/8 is: "+(20+-3*5/8));
		System.out.println("The result of 5+15/3*2-8%3 is: "+(5+15/3*2-8%3));
	}

}
